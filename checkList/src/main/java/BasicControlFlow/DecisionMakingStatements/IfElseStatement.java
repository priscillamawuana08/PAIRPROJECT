package BasicControlFlow.DecisionMakingStatements;

public class IfElseStatement {
    public static void main(String[] args) {

        int yam = 105;
        int plantain = 35;

        if(yam - plantain < 100){
            System.out.println("The total number left is 70");
        }
        else{
            System.out.println("The total is not expected");
        }
    }

}
