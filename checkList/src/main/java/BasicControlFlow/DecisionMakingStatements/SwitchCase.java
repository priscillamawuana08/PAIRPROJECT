package BasicControlFlow.DecisionMakingStatements;

public class SwitchCase {
    public static void main(String[] args) {
              int name =  4;

        switch(name){
            case 1:
                System.out.println("Number 1 is Priscilla");
                break;

            case 2:
                System.out.println("Number 2 is Isaac");
                break;

            case 3:
                System.out.println("Number 3 is Linda");
                break;

            case 4:
                System.out.println("Number 4 is Godfred");
                break;

            case 5:
                System.out.println("Number 5 is Bintu");
                break;

            case 6:
                System.out.println("Number 6 is David");
                break;

            default:
                System.out.println("Names are not in yet");
        }
    }
}
